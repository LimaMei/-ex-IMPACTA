dia = int(input('Escolha um número de 1 à 7:'))

if dia == 1:
    print('Hoje é Domingo')
elif dia == 2:
    print ('Hoje é Segunda')
elif dia == 3:
    print ('Hoje é terca')
elif dia == 4:
    print ('Hoje é quarta')
elif dia == 5:
    print ('Hoje é quinta')
elif dia == 6:
    print ('Hoje é sexta')
elif dia == 7:
    print ('Hoje é sabado')
elif dia >= 8:
    print ('Esse dia nao existe!')